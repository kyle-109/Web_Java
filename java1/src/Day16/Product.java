package Day16;

public class Product<T, S> {
	private T kind;
	private S model;
	public T getKind() {
		return kind;
	}
	public void setKind(T kind) {
		this.kind = kind;
	}
	public S getModel() {
		return model;
	}
	public void setModel(S model) {
		this.model = model;
	}
	
	
	
}
